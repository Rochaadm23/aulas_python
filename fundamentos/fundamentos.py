# Tipos Básicos

print(True)  # -> Booleano
print(False)  # -> Booleano
print(1.2 + 1)  # -> Float
print('Aqui eu falo na minha lingua')  # -> String
print('também funciona')  # -> String
print('você é ' + 3 * 'muito ' + 'legal')  # -> String
# print(3 + 'a') -> Ambiguidade
print([1, 2, 3])  # -> Lista
print({'nome': 'Fernando', 'idade': 30, 'salário': 20000.00})  # -> Dicionário
print(None)  # -> Nulo
